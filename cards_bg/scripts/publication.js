function applyFilters() {
    const yearFilter = document.getElementById("year-filter").value;
    const letterFilter = document.getElementById("letter-filter").value;
    const searchQuery = document.getElementById("search-filter").value.toLowerCase();

    let filteredPublications = publications;

    // Apply year filter
    if (yearFilter !== "all") {
        filteredPublications = filteredPublications.filter(pub => pub.year === yearFilter);
    }

    // Apply letter filter
    if (letterFilter !== "all") {
        filteredPublications = filteredPublications.filter(pub => 
            pub.title.charAt(0).toUpperCase() === letterFilter
        );
    }

    // Apply search filter (checks all available fields)
    if (searchQuery) {
        filteredPublications = filteredPublications.filter(pub => {
            const searchableText = [
                pub.title,
                pub.authors,
                pub.journal,
                pub.conference,
                pub.report,
                pub.location
            ]
                .filter(Boolean) // Remove undefined/null fields
                .join(" ")
                .toLowerCase();

            return searchableText.includes(searchQuery);
        });
    }

    fetchPublications(filteredPublications);
}

function resetFilters() {
    document.getElementById("year-filter").value = "all";
    document.getElementById("letter-filter").value = "all";
    document.getElementById("search-filter").value = "";
    fetchPublications();
}